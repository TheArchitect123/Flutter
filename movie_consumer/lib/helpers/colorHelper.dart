import 'package:flutter/material.dart';

class ColorHelper {
  static Color darkThemeBackground() {
    return Color.fromRGBO(33, 33, 33, 1);
  }

  static Color darkThemeTextColour() {
    return Color.fromRGBO(255, 255, 255, 1);
  }

  static Color darkThemeNavigationBar() {
    return Color.fromRGBO(33, 33, 32, 1);
  }
}
